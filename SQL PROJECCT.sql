CREATE TABLE BOOK(
       Book_ID SERIAL PRIMARY KEY ,
	   Title VARCHAR(100) ,
	   Author VARCHAR(100),
	   Genre VARCHAR(50) ,
	   Published_year INT ,
	   Price NUMERIC(10,2) ,
	   Stock INT
);

CREATE TABLE customers(
     Customer_ID SERIAL PRIMARY KEY ,
	 Name VARCHAR(50), 
	 email VARCHAR(100),
	 phone VARCHAR(100),
	 city VARCHAR(50),
	 country VARCHAR(100)
);

CREATE TABLE ORDERS(
       	Order_id SERIAL PRIMARY KEY ,
		Customer_ID INT REFERENCES customers(Customer_ID),
		Book_ID INT REFERENCES BOOK(Book_ID),
		Order_date DATE,
		Quantity INT, 
		Total_Amount NUMERIC(10, 2)
);

