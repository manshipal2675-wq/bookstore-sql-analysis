-- ADVANCED QUES 

--1 RETERIEVE THE TOTAL NUMBER OF BOOKS SOLD FOR EACH GENRE
SELECT b.genre, SUM(o.quantity) AS TOTAL_BOOK_SOLD
FROM ORDERS o
JOIN BOOK b 
ON b.book_id = o.book_id 
GROUP BY genre ;

--2 FIND THE AVERAGE PRICE OF BOOKS IN THE 'FANTASY' GENRE
SELECT AVG(price) as avg_price
FROM BOOK
WHERE genre = 'Fantasy' ;

--3 LIST ALL THE CUSTOMERS WHO PLACED AT LEAST 2 ORDERS 
SELECT o.customer_id, c.name, COUNT(o.order_id) AS TOTAL_ORDER
FROM ORDERS o
JOIN customers c
ON c.customer_id = o.customer_id 
GROUP BY o.customer_id , c.name
HAVING COUNT(o.order_id) >= 2;

--4 FIND THE MOST FREQUENTLY ORDERED BOOK
SELECT o.book_id, b.title, COUNT(o.order_id) AS ORDER_COUNT
FROM ORDERS o
JOIN book b
ON b.book_id = o.book_id
GROUP BY o.book_id, b.title
ORDER BY ORDER_COUNT DESC LIMIT 1;

--5 SHOW THE TOP 3 MOST EXPENSIVE BOOKS OF 'FANTASY' GENRE 
SELECT * FROM BOOK 
ORDER BY price DESC LIMIT 3;

--6 RETERIEVE THE TOTAL QUANTITY OF BOOKS SOLD BY EACH AUTHOR 
SELECT b.author, SUM(o.quantity) AS TOTAL_QUANTITY
FROM ORDERS o
JOIN BOOK b 
ON b.book_id = o.book_id 
GROUP BY b.author ;

--7 LIST THE CITIES WHERE CUSTOMERS WHO SPENT OVER $30 ARE LOCATED 
SELECT DISTINCT c.city, o.total_amount 
FROM ORDERS o
JOIN customers c 
ON c.customer_id = o.customer_id
WHERE o.total_amount > 30 ;

-- 8 FIND THE CUSTOMERS WHO SPENT MOST ON THE ORDERS 
SELECT c.name, c.customer_id, SUM(o.total_amount) AS TOTAL_SPENT
FROM ORDERS o
JOIN customers c
ON c.customer_id = o.customer_id
GROUP BY c.customer_id, c.name
ORDER BY TOTAL_SPENT DESC LIMIT 5 ;

--9 CALCULATE THE STOCK REMAINING AFTER THE FULFILLING ALL ORDERS 
SELECT b.book_id, b.stock, COALESCE(SUM(quantity), 0) AS ORDER_QUANTITY,
b.stock - COALESCE(SUM(quantity), 0) AS REMAINING_STOCK
FROM BOOK b 
LEFT JOIN ORDERS o
ON b.book_id = o.book_id
GROUP BY b.book_id 
ORDER BY book_id ;
